package com.txznet.music.data.entity;

/**
 * 二维码信息
 * <note>: 在从未绑定状态到绑定状态是通过wx.info.nick来进行回调的，这里采用了模拟数据进行回调因为这个响应是个string而非json</note>
 *
 * @author telen
 * @date 2019/1/16,16:51
 */
public class QrCodeInfo {

    /**
     * uint64Flag : 32
     * nick : 晴空一垩
     * issuccess : true
     * isbind : true
     * qrcode : http://weixin.qq.com/q/02NZBqE-FRea41uZhahscU
     */

    public int uint64Flag;
    public String nick;
    public boolean issuccess;
    public boolean isbind;
    public String qrcode;


    @Override
    public String toString() {
        return "QrCodeInfo{" +
                "uint64Flag=" + uint64Flag +
                ", nick='" + nick + '\'' +
                ", issuccess=" + issuccess +
                ", isbind=" + isbind +
                ", qrcode='" + qrcode + '\'' +
                '}';
    }
}
