public class TXZFlashPage {
	private String url;//闪屏页内容URL
	private int time;//闪屏页持续时间

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

    @Override
    public String toString() {
        return "TXZFlashPage{" +
                "url='" + url + '\'' +
                ", time=" + time +
                '}';
    }
}
