package com.txznet.launcher.domain.help.bean;

/**
 * Created by daviddai on 2018/8/27
 * 帮助界面item 的数据类。
 */
public class HelpCommand {
    // commands的类型，如"导航"
    private String type;
    // 类型的icon
    private int icon;
    // commands，多个command的字段。如"导航到东方明珠、我要回家／公司"
    private String commands;

    public HelpCommand(){
    }

    public HelpCommand(String type, String commands, int icon) {
        this.type = type;
        this.commands = commands;
        this.icon = icon;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getCommands() {
        return commands;
    }

    public void setCommands(String commands) {
        this.commands = commands;
    }

}
