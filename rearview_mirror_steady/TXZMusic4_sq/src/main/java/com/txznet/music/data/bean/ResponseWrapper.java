public class ResponseWrapper<T> {
    T data;
    long sessionId;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public long getSessionId() {
        return sessionId;
    }

    public void setSessionId(long sessionId) {
        this.sessionId = sessionId;
    }
}
