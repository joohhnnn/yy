public class DataWrapper<T> {

    private T data;

    private long sessionId;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public long getSessionId() {
        return sessionId;
    }

    public void setSessionId(long sessionId) {
        this.sessionId = sessionId;
    }

    @Override
    public String toString() {
        return "DataWrapper{" +
                "data=" + data +
                ", sessionId=" + sessionId +
                '}';
    }
}
