package com.txznet.txz.module.help;

import java.util.List;

public class TipsInfo {
    private int foreScene;//前台状态
    private int backgroundScene;//后台状态
    private List<String>  wpLists;//免唤醒词
    private boolean isNav;//是否在导航中
    private boolean isGetWXInfo;//是否收到微信消息
    private boolean isRecording;//微信是否在录音中
    private boolean isOpenWindows;//是否需要打开指令列表详情

    public int getForeScene() {
        return foreScene;
    }

    public void setForeScene(int foreScene) {
        this.foreScene = foreScene;
    }

    public int getBackgroundScene() {
        return backgroundScene;
    }

    public void setBackgroundScene(int backgroundScene) {
        this.backgroundScene = backgroundScene;
    }

    public List<String> getWpLists() {
        return wpLists;
    }

    public void setWpLists(List<String> wpLists) {
        this.wpLists = wpLists;
    }

    public boolean isNav() {
        return isNav;
    }

    public void setNav(boolean isNav) {
        this.isNav = isNav;
    }

    public boolean isGetWXInfo() {
        return isGetWXInfo;
    }

    public void setGetWXInfo(boolean isGetWXInfo) {
        this.isGetWXInfo = isGetWXInfo;
    }

    public boolean isRecording() {
        return isRecording;
    }

    public void setRecording(boolean isRecording) {
        this.isRecording = isRecording;
    }

    public boolean isOpenWindows() {
        return isOpenWindows;
    }

    public void setOpenWindows(boolean openWindows) {
        isOpenWindows = openWindows;
    }
}
