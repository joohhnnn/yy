package com.txznet.txz.util.runnables;

public abstract class Runnable3<T1, T2, T3> implements Runnable {
	protected T1 mP1;
	protected T2 mP2;
	protected T3 mP3;

	public Runnable3(T1 p1, T2 p2, T3 p3) {
		mP1 = p1;
		mP2 = p2;
		mP3 = p3;
	}

	public void update(T1 p1, T2 p2, T3 p3) {
		mP1 = p1;
		mP2 = p2;
		mP3 = p3;
	}
}
