package com.txznet.txz.component.asr.mix;

import com.txznet.txz.component.asr.IAsr.AsrOption;

public abstract class AsrCallBackProxy implements IAsrCallBackProxy{
	protected AsrOption mAsrOption = null;
	
	public AsrCallBackProxy(){
	}
	
	public final void setAsrOption(AsrOption asrOption){
		mAsrOption = asrOption;
	}
	
	public final AsrOption getAsrOption(){
		return mAsrOption;
	}
	@Override
	public void onMonitor(String attr) {}
}
