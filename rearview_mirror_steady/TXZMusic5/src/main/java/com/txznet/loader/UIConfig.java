public class UIConfig {
    public int x;
    public int y;
    public int width;
    public int height;
    public int gravity;

    @Override
    public String toString() {
        return "UIConfig{" +
                "x=" + x +
                ", y=" + y +
                ", width=" + width +
                ", height=" + height +
                ", gravity=" + gravity +
                '}';
    }
}
