package com.txznet.music.bean.response;

import java.util.List;

/**
 * @author telenewbie
 * @version 创建时间：2016年2月23日 下午6:16:00
 * 
 */
public class ResponseURL {

	private String strUrl;
	private int iExpTime;
	private long audioId;
	private List<String> arrBackUpUrl;
	private int errCode;
	

	public int getErrCode() {
		return errCode;
	}

	public void setErrCode(int errCode) {
		this.errCode = errCode;
	}

	public long getAudioId() {
		return audioId;
	}

	public void setAudioId(long audioId) {
		this.audioId = audioId;
	}

	public String getStrUrl() {
		return strUrl;
	}

	public void setStrUrl(String strUrl) {
		this.strUrl = strUrl;
	}

	public int getiExpTime() {
		return iExpTime;
	}

	public void setiExpTime(int iExpTime) {
		this.iExpTime = iExpTime;
	}

	public List<String> getArrBackUpUrl() {
		return arrBackUpUrl;
	}

	public void setArrBackUpUrl(List<String> arrBackUpUrl) {
		this.arrBackUpUrl = arrBackUpUrl;
	}

	@Override
	public String toString() {
		return "ResponseURL [strUrl=" + strUrl + ", iExpTime=" + iExpTime
				+ ", audioId=" + audioId + ", arrBackUpUrl=" + arrBackUpUrl
				+ "]";
	}

}
