package com.txznet.music.data.kaola.net.bean;

public class KaolaCategory {
    /**
     * cid": 1232,//分类id -1代表推荐 -2代表传统广播
     * "name":"搞笑",//分类名称
     * "hasSub":0// 0没有 1 ?
     * "img":""//图片
     */

    private long cid;//分类id -1代表推荐 -2代表传统广播
    private String name;//:"搞笑",//分类名称
    private int hasSub;//:0// 0没有 1 ?
    private String img;//:""//图片

    public long getCid() {
        return cid;
    }

    public void setCid(long cid) {
        this.cid = cid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHasSub() {
        return hasSub;
    }

    public void setHasSub(int hasSub) {
        this.hasSub = hasSub;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
